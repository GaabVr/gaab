public class variaveis {
    public static void main(String[] args) throws Exception {
        // tipo nome
        // variaveis inteiras
        byte numero = 125;
        short numeropq = 32;
        System.out.println(numero);
        System.out.println(numeropq);
        // variaveis de ponto flutuante
        float n = 2.0f;
        System.out.println(n);
    }

    
}
